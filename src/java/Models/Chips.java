package Models;

public class Chips {

    private String Codigo, CodPaquete, CodEmpleado, ICCID, Fecha, CodModalidad, Estado;

    public String getCodigo() {
        return Codigo;
    }

    public void setCodigo(String Codigo) {
        this.Codigo = Codigo;
    }

    public String getCodPaquete() {
        return CodPaquete;
    }

    public void setCodPaquete(String CodPaquete) {
        this.CodPaquete = CodPaquete;
    }

    public String getCodEmpleado() {
        return CodEmpleado;
    }

    public void setCodEmpleado(String CodEmpleado) {
        this.CodEmpleado = CodEmpleado;
    }

    public String getICCID() {
        return ICCID;
    }

    public void setICCID(String ICCID) {
        this.ICCID = ICCID;
    }

    public String getEstado() {
        return Estado;
    }

    public void setEstado(String Estado) {
        this.Estado = Estado;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }

    public String getCodModalidad() {
        return CodModalidad;
    }

    public void setCodModalidad(String CodModalidad) {
        this.CodModalidad = CodModalidad;
    }

}
